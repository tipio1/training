// Variables
// A variable is like a box where you can put anything you want into.
// A variable must me declared (building the box) and assigned (put stuff inside - give it a value)
// const and let are keywords to declare a variable : a let can be reassigned but a const can't

let a = 3;
let b;
b = 2;
console.log(a);
console.log(b);

// A variable may be of different types (string, number, boolean, object, null, undefined)
let hello = "Hello";
console.log(hello);     // Hello    // variable is a string
console.log("hello");   // hello    // not a variable

let c = 42;                         // variable is a number
let d = true;                       // variable is a boolean
console.log(c, d); 

let e = c + 1;
console.log(e);